/*
 * Complete the 'swapNodes' function below.
 *
 * The function is expected to return a 2D_INTEGER_ARRAY.
 * The function accepts following parameters:
 *  1. 2D_INTEGER_ARRAY indexes
 *  2. INTEGER_ARRAY queries
 */

void in_order(vector<vector<int>>& indexes, const int n, const int k, const int depth, vector<int>& res) {
	if (n<0) {
		return;
	}

	if (depth%k==0) {
		const int tmp = indexes[n-1][0];
		indexes[n-1][0] = indexes[n-1][1];
		indexes[n-1][1] = tmp;
	}

	const int l = indexes[n-1][0];
	const int r = indexes[n-1][1];

	in_order(indexes,l,k,depth+1,res);
	res.push_back(n);
	in_order(indexes,r,k,depth+1,res);
}

vector<vector<int>> swapNodes(vector<vector<int>> indexes, vector<int> queries) {

	vector<vector<int>> res;

	for (const auto k: queries) {
		vector<int> traversal;

		in_order(indexes,1,k,1,traversal);
		res.push_back(traversal);
	}

	return res;
}

