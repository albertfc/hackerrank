// Complete the alternatingCharacters function below.
int alternatingCharacters(string s) {
	int removes = 0;
	char last = s[0];
	for( size_t i = 1; i<s.size(); i++ ) {
		if( s[i] == last ) {
			removes++;
		} else {
			last = s[i];
		}
	}
	return removes;
}


