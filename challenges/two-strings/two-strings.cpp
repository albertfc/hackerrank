using namespace std;

array<bool,26> tab;
// Complete the twoStrings function below.
string twoStrings(string s1, string s2) {
	tab = {false};

	for( const char c: s1 ) {
		tab[c-'a'] = true;
	}

	for( const char c: s2 ) {
		if( tab[c-'a'] )
			return "YES";
	}

	return "NO";
}

