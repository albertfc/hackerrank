/*
 * Complete the 'superDigit' function below.
 *
 * The function is expected to return an INTEGER.
 * The function accepts following parameters:
 *  1. STRING n
 *  2. INTEGER k
 */

int superDigit(string n, int k) {
	if (n.size() == 1 && k == 1) {
		return stoi(n);
	}

	// add n chars and multiply by k
	int res = 0;
	for (const auto& c : n) {
		res += c-'0';
	}
	res *= k;
	// transform to string and call recursevely with k=1
	return superDigit( std::to_string(res), 1 );
}

