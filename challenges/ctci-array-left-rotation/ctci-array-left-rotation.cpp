// Complete the rotLeft function below.
vector<int> rotLeft(vector<int> a, int d) {

	vector<int> res( a.size() );
	size_t i = d % a.size();
	for (size_t j = 0; j < a.size(); ++j) {
		res[j] = a[i];
		i = (i+1) % a.size();
	}
	return res;
}

