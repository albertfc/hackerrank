// Complete the isBalanced function below.
string isBalanced(string s) {
	stack<char> st;
	for( const char c: s ) {
		switch (c) {
			case '{': st.push( '}' ); break;
			case '[': st.push( ']' ); break;
			case '(': st.push( ')' ); break;
			default:
				if( st.empty() || st.top() != c )
					return "NO";
				st.pop();
		}
	}
	return st.empty() ? "YES" : "NO";
}


